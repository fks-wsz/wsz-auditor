import type { Express } from 'express';
import chokidar from 'chokidar';
import { getFileContent, getJsonFileContent } from 'wsz-auditor-shared/node';
import webpackClientConfig from './webpack.config.client';
import webpackServerBundleConfig from './webpack.config.server-bundle';
import webpack from 'webpack';
import type { EntryObject } from 'webpack';
import webpackDevMiddleware from 'webpack-dev-middleware';
import { join } from 'path';
import webpackHotMiddleware from 'webpack-hot-middleware';

async function setupDevServer(app: Express, templatePath: string, callback: (...args: any[]) => void): Promise<void> {
  let clientManifest: Record<string, any>;
  let serverBundle: Record<string, any>;
  let templateStr: string;

  let ready: () => void;

  const readyPromise = new Promise<void>((r) => {
    ready = r;
  });

  const update = () => {
    if (clientManifest && serverBundle) {
      ready();
      callback({ clientManifest, serverBundle, templateStr });
    }
  };

  templateStr = await getFileContent(templatePath);
  chokidar.watch(templatePath).on('change', async () => {
    templateStr = await getFileContent(templatePath);
    console.log('index.html template updated.');
    update();
  });

  webpackClientConfig.entry = webpackClientConfig.entry || {};
  (webpackClientConfig.entry as EntryObject).client = [
    'webpack-hot-middleware/client',
    (webpackClientConfig.entry as EntryObject).client as string,
  ];
  webpackClientConfig.output = webpackClientConfig.output || {};
  webpackClientConfig.output.filename = '[name].js';
  webpackClientConfig.plugins = webpackClientConfig.plugins || [];
  webpackClientConfig.plugins.push(new webpack.HotModuleReplacementPlugin(), new webpack.NoEmitOnErrorsPlugin());

  const clientCompiler = webpack(webpackClientConfig);
  const devMiddleware = webpackDevMiddleware(clientCompiler, {
    publicPath: webpackClientConfig.output.publicPath,
  });

  app.use(devMiddleware);

  clientCompiler.hooks.done.tap('done', (stats) => {
    let statsJson;
    statsJson = stats.toJson();
    (statsJson.errors = statsJson.errors || []).forEach((err) => console.error(err));
    (statsJson.warnings = statsJson.warnings || []).forEach((err) => console.warn(err));
    if (statsJson.errors.length) return;
    const clientManifestPath = join(webpackClientConfig.output?.path || __dirname, 'vue-ssr-client-manifest.json');
    clientManifest = JSON.parse(devMiddleware.context.outputFileSystem.readFileSync(clientManifestPath, 'utf-8'));
    update();
  });

  app.use(webpackHotMiddleware(clientCompiler, { heartbeat: 5000 }));

  const serverBundleCompiler = webpack(webpackServerBundleConfig);
  serverBundleCompiler.watch({}, async (err, stats) => {
    if (err) throw err;
    if (!stats) return;
    let statsJson;
    statsJson = stats.toJson();
    statsJson.errors = statsJson.errors || [];
    if (statsJson.errors.length) return;

    // read bundle generated by vue-ssr-webpack-plugin
    serverBundle = await getJsonFileContent(
      join(webpackServerBundleConfig.output?.path || __dirname, 'vue-ssr-server-bundle.json'),
    );
    update();
  });

  return readyPromise;
}

export default setupDevServer;
